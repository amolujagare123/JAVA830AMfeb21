package Inheritance.Polymorphism;

public class PrintlnExample {

    public static void main(String[] args) {

        // println is a method taking string as an argument
        System.out.println("amol");

        int i =10 ;
        System.out.println(i); // int as an argument

        double d = 11.23;
        System.out.println(d); // double as an argument

        boolean result =true;
        System.out.println(result); // boolean as an argument
    }

}
