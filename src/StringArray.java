public class StringArray {

    public static void main(String[] args) {

        String[] s = new String[5];

        s[0] = "amol";
        s[1] = "rahul";
        s[2] = "manish";
        s[3] = "ashish";
        s[4] = "sagar";

        for(int i=0;i<s.length;i++)
        {
            System.out.println(s[i]);
        }
    }
}
